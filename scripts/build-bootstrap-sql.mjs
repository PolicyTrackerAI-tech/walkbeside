/**
 * Generate supabase/BOOTSTRAP.sql — schema.sql followed by every migration in
 * order, concatenated into one file you paste ONCE into the SQL editor of a
 * fresh production Supabase project.
 *
 * All statements are idempotent (if-not-exists / drop-if-exists /
 * create-or-replace / revoke+grant), so re-running is safe.
 *
 * Re-generate after adding a migration:  node scripts/build-bootstrap-sql.mjs
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join } from "node:path";

const supa = join(process.cwd(), "supabase");
const migDir = join(supa, "migrations");

const migrations = readdirSync(migDir)
  .filter((f) => f.endsWith(".sql"))
  .sort(); // date-prefixed → chronological

const bar = "=".repeat(78);
let out = `-- ${bar}
-- Honest Funeral — PRODUCTION DATABASE BOOTSTRAP
-- GENERATED by scripts/build-bootstrap-sql.mjs — do not hand-edit.
-- Re-generate:  node scripts/build-bootstrap-sql.mjs
--
-- HOW TO USE: open a fresh prod Supabase project → SQL Editor → paste this
-- ENTIRE file → Run. It applies schema.sql then every migration in order.
-- Idempotent, so safe to re-run. See docs/PROD_SETUP.md for the full checklist
-- (storage bucket, magic-link auth, env vars, DNS) — this file is just the DB.
-- ${bar}
`;

function section(title, body) {
  out += `\n\n-- ${bar}\n-- ${title}\n-- ${bar}\n\n${body.trim()}\n`;
}

section("schema.sql", readFileSync(join(supa, "schema.sql"), "utf8"));
for (const m of migrations) {
  section(`migrations/${m}`, readFileSync(join(migDir, m), "utf8"));
}

writeFileSync(join(supa, "BOOTSTRAP.sql"), out);
console.log(
  `Wrote supabase/BOOTSTRAP.sql — schema.sql + ${migrations.length} migrations:`,
);
for (const m of migrations) console.log(`  - ${m}`);
