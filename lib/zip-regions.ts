/**
 * Look up region info for a given zip. Returns null if the zip's
 * 3-digit prefix isn't in our table — caller falls back to the
 * 1-digit prefix logic.
 */
export function regionForZip(zip: string): ZipRegion | null {
  if (!zip || zip.length < 3) return null;
  return ZIP_REGIONS[zip.slice(0, 3)] ?? null;
}
