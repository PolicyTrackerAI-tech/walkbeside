/**
 * Metro-area zip prefix lookup for regional pricing precision.
 *
 * Maps the first 3 digits of a US zip code to:
 *   - multiplier: cost-of-living adjustment vs. national average (1.00)
 *   - metro: human-readable metro/area label (e.g., "Manhattan, NY")
 *
 * This replaces the original 3-bucket multiplier (high-coastal /
 * baseline / low-COL) with ~150 metro-level entries covering most of
 * the US population. Where a zip's 3-digit prefix isn't in the table,
 * regionMultiplier() falls back to the 1-digit prefix logic below.
 *
 * Multipliers are best-fit estimates calibrated against:
 *   - BLS Regional Price Parities by metro
 *   - General cost-of-living indexes (Numbeo, BestPlaces, etc.)
 *   - Industry knowledge of funeral-pricing variation per region
 *
 * NOT validated against actual General Price Lists — these multipliers
 * power the "modeled" data tier (dataSourceForZip's static answer).
 * Regions upgrade to "verified"/"community" via regional_benchmarks
 * override rows, founder-promoted at n≥5 on /admin/benchmarks and read
 * by lib/benchmarks-store.ts (metro-scoped rows match on the exact
 * metro labels below).
 *
 * Range: 0.75 (rural Mississippi / Appalachian KY) to 1.50 (Manhattan,
 * SF, Honolulu).
 */

